fn encode_rle(bytes: &[u8]) -> String {
    let mut output = String::new();
    let mut index = 0;
    while index < bytes.len() {
        let byte = bytes[index];
        let mut end = index + 1;
        while end < bytes.len() && bytes[end] == byte {
            end += 1;
        }
        if !output.is_empty() {
            output.push(',');
        }
        output.push_str(&format!("{byte:02x}*{}", end - index));
        index = end;
    }
    output
}

