    fn mark(&mut self, oid: Oid, feature: &'static str) {
        self.feature_oids.insert(oid, feature);
    }

    fn git(&self, args: &[&str], input: Option<&[u8]>, timestamp: i64) -> Result<Vec<u8>> {
        let mut command = self.git_command(timestamp);
        command
            .arg("--git-dir")
            .arg(&self.git_dir)
            .args(args)
            .stdout(Stdio::piped())
            .stderr(Stdio::piped());
        if input.is_some() {
            command.stdin(Stdio::piped());
        }
        let mut child = command.spawn()?;
        if let Some(bytes) = input {
            child.stdin.take().expect("piped stdin").write_all(bytes)?;
        }
        let output = child.wait_with_output()?;
        if !output.status.success() {
            return Err(io::Error::other(format!(
                "git {} failed: {}",
                args.join(" "),
                String::from_utf8_lossy(&output.stderr).trim()
            ))
            .into());
        }
        Ok(output.stdout)
    }

