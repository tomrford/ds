fn raw_commit(
    tree: Oid,
    parents: &[Oid],
    extra_header: Option<&[u8]>,
    author_name: &[u8],
    message: &[u8],
    timestamp: i64,
) -> Vec<u8> {
    let mut payload = Vec::new();
    writeln!(payload, "tree {}", oid_hex(tree)).expect("Vec write");
    for parent in parents {
        writeln!(payload, "parent {}", oid_hex(*parent)).expect("Vec write");
    }
    payload.extend_from_slice(b"author ");
    payload.extend_from_slice(author_name);
    writeln!(payload, " <oracle@example.invalid> {timestamp} +0000").expect("Vec write");
    writeln!(
        payload,
        "committer Git Oracle <oracle@example.invalid> {timestamp} +0000"
    )
    .expect("Vec write");
    if let Some(header) = extra_header {
        payload.extend_from_slice(header);
    }
    payload.push(b'\n');
    payload.extend_from_slice(message);
    payload
}

fn with_mergetag(commit: &[u8], tag: &[u8]) -> Result<Vec<u8>> {
    let header_end = commit
        .windows(2)
        .position(|window| window == b"\n\n")
        .ok_or_else(|| io::Error::other("Git merge commit lacks header terminator"))?;
    let mut payload = Vec::with_capacity(commit.len() + tag.len() + 16);
    payload.extend_from_slice(&commit[..header_end]);
    payload.extend_from_slice(b"\nmergetag ");
    let mut lines = tag.split(|byte| *byte == b'\n');
    payload.extend_from_slice(lines.next().unwrap_or_default());
    payload.push(b'\n');
    for line in lines {
        payload.push(b' ');
        payload.extend_from_slice(line);
        payload.push(b'\n');
    }
    payload.extend_from_slice(&commit[header_end + 1..]);
    Ok(payload)
}

fn pretty_commit_references(payload: &[u8]) -> Result<(Oid, Vec<Oid>)> {
    let mut tree = None;
    let mut parents = Vec::new();
    for line in payload.split(|byte| *byte == b'\n') {
        if line.is_empty() {
            break;
        }
        if let Some(hex) = line.strip_prefix(b"tree ") {
            tree = Oid::from_hex(hex);
        } else if let Some(hex) = line.strip_prefix(b"parent ") {
            parents.push(
                Oid::from_hex(hex)
                    .ok_or_else(|| io::Error::other("invalid cat-file parent oid"))?,
            );
        }
    }
    Ok((
        tree.ok_or_else(|| io::Error::other("cat-file commit lacks tree"))?,
        parents,
    ))
}

fn pretty_tree_references(payload: &[u8]) -> Result<BTreeSet<(ReferenceKind, Oid)>> {
    payload
        .split(|byte| *byte == b'\n')
        .filter(|line| !line.is_empty())
        .map(|line| {
            let before_tab = line
                .split(|byte| *byte == b'\t')
                .next()
                .ok_or_else(|| io::Error::other("cat-file tree line lacks tab"))?;
            let fields = before_tab.split(|byte| *byte == b' ').collect::<Vec<_>>();
            if fields.len() != 3 {
                return Err(io::Error::other("invalid cat-file tree line").into());
            }
            let kind = match fields[0] {
                b"100644" => ReferenceKind::Blob,
                b"100755" => ReferenceKind::Executable,
                b"120000" => ReferenceKind::Symlink,
                b"040000" => ReferenceKind::Tree,
                b"160000" => ReferenceKind::Gitlink,
                _ => return Err(io::Error::other("invalid cat-file tree mode").into()),
            };
            let oid = Oid::from_hex(fields[2])
                .ok_or_else(|| io::Error::other("invalid cat-file tree oid"))?;
            Ok((kind, oid))
        })
        .collect()
}

fn read_line(reader: &mut impl Read) -> Result<Vec<u8>> {
    let mut line = Vec::new();
    let mut byte = [0];
    loop {
        reader.read_exact(&mut byte)?;
        if byte[0] == b'\n' {
            return Ok(line);
        }
        line.push(byte[0]);
    }
}

fn parse_oid(bytes: &[u8]) -> Result<Oid> {
    let hex = bytes.strip_suffix(b"\n").unwrap_or(bytes);
    Oid::from_hex(hex).ok_or_else(|| io::Error::other("git emitted an invalid SHA-1").into())
}

fn oid_hex(oid: Oid) -> String {
    encode_hex(&oid.0)
}

fn encode_hex(bytes: &[u8]) -> String {
    const HEX: &[u8; 16] = b"0123456789abcdef";
    let mut output = String::with_capacity(bytes.len() * 2);
    for byte in bytes {
        output.push(char::from(HEX[usize::from(byte >> 4)]));
        output.push(char::from(HEX[usize::from(byte & 0x0f)]));
    }
    output
}

const fn kind_name(kind: ObjectKind) -> &'static str {
    match kind {
        ObjectKind::Blob => "blob",
        ObjectKind::Tree => "tree",
        ObjectKind::Commit => "commit",
    }
}

